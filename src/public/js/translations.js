// Function to get current language
function getCurrentLanguage() {
  return localStorage.getItem('preferred_language') || 'english';
}

// Function to set language
function setLanguage(language) {
  localStorage.setItem('preferred_language', language);
  updatePageContent();
}

// Function to toggle language
function toggleLanguage() {
  const currentLang = getCurrentLanguage();
  const newLang = currentLang === 'english' ? 'hindi' : 'english';
  setLanguage(newLang);
  
  // Update button text
  const langButtons = document.querySelectorAll('.language-toggle');
  langButtons.forEach(button => {
    button.innerHTML = `<i class="fas fa-language me-1"></i> ${translations[newLang].changeToHindi}`;
  });
}

// Function to update page content
function updatePageContent() {
  const currentLang = getCurrentLanguage();
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[currentLang][key]) {
      // Handle different element types
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        if (element.type !== 'submit' && element.type !== 'button') {
          // For input fields, update placeholder
          element.placeholder = translations[currentLang][key];
        } else {
          // For buttons, update value and text
          element.value = translations[currentLang][key];
          element.textContent = translations[currentLang][key];
        }
      } else if (element.tagName === 'SELECT') {
        // For select elements, update options
        Array.from(element.options).forEach(option => {
          const optionKey = option.getAttribute('data-i18n');
          if (optionKey && translations[currentLang][optionKey]) {
            option.text = translations[currentLang][optionKey];
          }
        });
      } else {
        // For all other elements, update text content
        element.textContent = translations[currentLang][key];
      }
    }
  });

  // Update page title if it has translation
  const pageTitle = document.querySelector('title');
  if (pageTitle && pageTitle.getAttribute('data-i18n')) {
    const titleKey = pageTitle.getAttribute('data-i18n');
    if (translations[currentLang][titleKey]) {
      pageTitle.textContent = translations[currentLang][titleKey];
    }
  }

  // Update all buttons with the language toggle class
  const langButtons = document.querySelectorAll('.language-toggle');
  langButtons.forEach(button => {
    button.innerHTML = `<i class="fas fa-language me-1"></i> ${translations[currentLang].changeToHindi}`;
  });

  // Update meta tags if they have translations
  const metaTags = document.querySelectorAll('meta[data-i18n]');
  metaTags.forEach(meta => {
    const metaKey = meta.getAttribute('data-i18n');
    if (translations[currentLang][metaKey]) {
      meta.setAttribute('content', translations[currentLang][metaKey]);
    }
  });

  // Dispatch a custom event to notify that language has been updated
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: currentLang } }));
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  // Set initial language from localStorage or browser preference
  const browserLang = navigator.language || navigator.userLanguage;
  const initialLang = localStorage.getItem('preferred_language') || 
                     (browserLang.startsWith('hi') ? 'hindi' : 'english');
  setLanguage(initialLang);
}); 